parasails.registerPage('sensor-detail', {
  //  ╦╔╗╔╦╔╦╗╦╔═╗╦    ╔═╗╔╦╗╔═╗╔╦╗╔═╗
  //  ║║║║║ ║ ║╠═╣║    ╚═╗ ║ ╠═╣ ║ ║╣
  //  ╩╝╚╝╩ ╩ ╩╩ ╩╩═╝  ╚═╝ ╩ ╩ ╩ ╩ ╚═╝
  data: {
    syncing: false,
    cloudError: '',

    modal:'',
    sensorToRemove: undefined,

    formData: {},
    formRules: {},
    formErrors: {},
  },

  //  ╦  ╦╔═╗╔═╗╔═╗╦ ╦╔═╗╦  ╔═╗
  //  ║  ║╠╣ ║╣ ║  ╚╦╝║  ║  ║╣
  //  ╩═╝╩╚  ╚═╝╚═╝ ╩ ╚═╝╩═╝╚═╝
  beforeMount: function() {
    // Attach any initial data from the server.
    _.extend(this, SAILS_LOCALS);
  },
  mounted: async function() {
    //…
  },

  //  ╦╔╗╔╔╦╗╔═╗╦═╗╔═╗╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
  //  ║║║║ ║ ║╣ ╠╦╝╠═╣║   ║ ║║ ║║║║╚═╗
  //  ╩╝╚╝ ╩ ╚═╝╩╚═╩ ╩╚═╝ ╩ ╩╚═╝╝╚╝╚═╝
  methods: {
    //…
    clickOpenRemoveModalButton: async function(sensor) {
      this.modal = 'remove';
      this.formData = {
        id: sensor.id,
        _sensor: _.extend({}, sensor),
      };
    },

    closeRemoveModal: async function() {
      this.modal = '';
      this._resetForms();
    },

    submittedRemoveSensorForm: function() {
      // Redirect to the device detail page on success.
      // > (Note that we re-enable the syncing state here.  This is on purpose--
      // > to make sure the spinner stays there until the page navigation finishes.)
      this.syncing = true;
      window.location = '/devices/'+encodeURIComponent(this.device.deviceId);
    },

    _resetForms: function() {
      this.syncing = false;
      this.cloudError = '';
      this.formData = {};
      this.formRules = {};
      this.formErrors = {};
    },
  }
});
